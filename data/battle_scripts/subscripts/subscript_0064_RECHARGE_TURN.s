.include "asm/include/battle_commands.inc"

.data

_000:
    CompareVarToValue OPCODE_NEQ, BSCRIPT_VAR_MOVE_NO_CUR, MOVE_HYPER_BEAM, _check_giga
    CompareMonDataToValue OPCODE_EQU, BATTLER_CATEGORY_DEFENDER, BMON_DATA_HP, 0, _end

_check_giga:
    CompareVarToValue OPCODE_NEQ, BSCRIPT_VAR_MOVE_NO_CUR, MOVE_GIGA_IMPACT, _set_recharge
    CompareMonDataToValue OPCODE_EQU, BATTLER_CATEGORY_DEFENDER, BMON_DATA_HP, 0, _end

_set_recharge:
    UpdateMonData OPCODE_FLAG_ON, BATTLER_CATEGORY_ATTACKER, BMON_DATA_STATUS2, STATUS2_RECHARGE
    UpdateVarFromVar OPCODE_SET, BSCRIPT_VAR_ATTACKER_LOCKED_MOVE, BSCRIPT_VAR_MOVE_NO_CUR
    UpdateMonDataFromVar OPCODE_SET, BATTLER_CATEGORY_ATTACKER, BMON_DATA_RECHARGE, BSCRIPT_VAR_TOTAL_TURNS

_end:
    End
